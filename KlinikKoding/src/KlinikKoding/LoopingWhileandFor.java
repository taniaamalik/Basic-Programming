package KlinikKoding;

public class LoopingWhileandFor {

    public static void main(String[] args) {
        int a = 0;
        while (a < 7) {
            System.out.println(a);
            a++;
        }
        System.out.println("===============");
        for (int i = 0; i < 7; i++) {
            System.out.println(i);
        }
    }
}