    private void formKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyReleased
        int zeroPos = getZeroPos();
        
        switch (evt.getKeyCode()){
            case KeyEvent.VK_RIGHT:
                if(!Arrays.asList(0,3,6).contains(zeroPos)) {
                    swap(zeroPos, zeroPos - 1);
                }
                
                break;
            case KeyEvent.VK_LEFT:
                if(!Arrays.asList(2,5,8).contains(zeroPos)) {
                    swap(zeroPos, zeroPos + 1);
                }
                
                break;
            case KeyEvent.VK_UP:
                if(!Arrays.asList(6,7,8).contains(zeroPos)) {
                    swap(zeroPos, zeroPos + 3);
                }
                
                break;
            case KeyEvent.VK_DOWN:
                if(!Arrays.asList(0,1,2).contains(zeroPos)) {
                    swap(zeroPos, zeroPos - 3);
                }
                
                break;
        }
        draw();
        
        if(puzzleComplete()){
            long elapsed=(System.currentTimeMillis() - startTime) / 1000;
            JOptionPane.showMessageDialog(getContentPane(), "Congratulations! You completed the puzzle in " + stepCount + " steps and in " + elapsed + " seconds!");
        }
    }//GEN-LAST:event_formKeyReleased

