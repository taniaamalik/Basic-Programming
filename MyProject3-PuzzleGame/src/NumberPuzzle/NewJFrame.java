    private void newGame(){
        ArrayList<Integer> numberList=
              new ArrayList<>(Arrays.asList(0,1,2,3,4,5,6,7,8));
        
        for (int i = 0; i < labels.length; i++) {
            int randomPos=(int) (Math.random()*numberList.size());
            numbers[i]=numberList.get(randomPos);
            
            numberList.remove(randomPos);
            
        }
        
        stepCount = 0;
        startTime = System.currentTimeMillis();
    }
    
    private void draw(){
        for (int i = 0; i < labels.length; i++) {
            labels[i].setText(numbers[i] > 0 ? String.valueOf(numbers[i]) : "");
            
        }
    }
    
    private void swap(int a, int b){
        int temp=numbers[a];
        numbers[a]=numbers[b];
        numbers[b]=temp;
        
        stepCount++;
    }
    
    private int getZeroPos(){
        for (int i = 0; i < labels.length; i++) {
            if (numbers[i] == 0) {
                return i;
            }
        }
        
        return -1;
    }
    
    private boolean puzzleComplete() {
        for (int i = 0; i < numbers.length-1; i++) {
            if(numbers[i] != i + 1){
                return false;
            }
        }
        return true;
    }
    
